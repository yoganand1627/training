package gov.georgia.dhr.dfcs.sacwis.dao.workload;

import gov.georgia.dhr.dfcs.sacwis.core.base.BaseValueBean;

/**
 * This PrincipalListDB Performs the database activities to set all the values and populate on the PrincipalCaseHistory
 * page.
 *
 * @author Vijaya Anand
 * @version 1.0
 *          <p/>
 *          Change History: Date      User         Description -------  -----------  ----------------------------------------------
 *          04/08/05  ANANDV       SIR 23522 - PrincipalListDB for PrincipalCaseHistory Page
 */

public class PrincipalListDB
        extends BaseValueBean {

  private int stageId;
  private String stage;
  private String ovrDisposition;
  private String personName;
  private int personID;
  private int age;
  private String gender;
  private String role;
  private String relation;
  private java.util.Date dateOfBirth;

  public PrincipalListDB() {
  }

  /** @return int */
  public int getStageId() {
    return stageId;
  }

  /** @param stageId  */
  public void setStageId(int stageId) {
    this.stageId = stageId;
  }

  /** @return String */
  public String getStage() {
    return stage;
  }

  /** @param stage  */
  public void setStage(String stage) {
    this.stage = stage;
  }

  /** @return String */
  public String getOvrDisposition() {
    return ovrDisposition;
  }

  /** @param ovrDisposition  */
  public void setOvrDisposition(String ovrDisposition) {
    this.ovrDisposition = ovrDisposition;
  }

  /** @return String */
  public String getPersonName() {
    return personName;
  }

  /** @param personName  */
  public void setPersonName(String personName) {
    this.personName = personName;
  }

  /** @return int */
  public int getPersonID() {
    return personID;
  }

  /** @param personID  */
  public void setPersonID(int personID) {
    this.personID = personID;
  }

  /** @return int */
  public int getAge() {
    return age;
  }

  /** @param age  */
  public void setAge(int age) {
    this.age = age;
  }

  /** @return String */
  public String getGender() {
    return gender;
  }

  /** @param gender  */
  public void setGender(String gender) {
    this.gender = gender;
  }

  /** @return Date */
  public java.util.Date getDateOfBirth() {
    return dateOfBirth;
  }

  /** @param dateOfBirth  */
  public void setDateOfBirth(java.util.Date dateOfBirth) {
    this.dateOfBirth = dateOfBirth;
  }

  /** @return String */
  public String getRole() {
    return role;
  }

  /** @param role  */
  public void setRole(String role) {
    this.role = role;
  }

  /** @return String */
  public String getRelation() {
    return relation;
  }

  /** @param relation  */
  public void setRelation(String relation) {
    this.relation = relation;
  }

}