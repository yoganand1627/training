package gov.georgia.dhr.dfcs.sacwis.dao.servicedelivery;

import java.util.Date;

import gov.georgia.dhr.dfcs.sacwis.core.base.BaseValueBean;

/** @todo add javadocs */

/**
 * Holds all data pertaining to a Family Plan Task.
 *
 * @author Jason Rios, February 6th, 2003
 */
public class FamilyPlanTaskValueBean extends BaseValueBean {
  // instance variables
  private int caseId;
  private int stageId;
  private int familyPlanEventId;
  private int familyPlanItemId;
  private int familyPlanTaskId;
  private Date familyPlanTaskDateLastUpdate;
  private Date dateTaskApproved;
  private boolean courtOrderedTask;
  private boolean courtMandatedClosure;
  private String task;
  private String progress;
  private org.exolab.castor.types.Date dateTaskCreated;
  private org.exolab.castor.types.Date dateTaskCompleted;
  private org.exolab.castor.types.Date dateCourtAction;

  /** Constructor */
  public FamilyPlanTaskValueBean() {
    caseId = 0;
    stageId = 0;
    familyPlanEventId = 0;
    familyPlanItemId = 0;
    familyPlanTaskId = 0;
    familyPlanTaskDateLastUpdate = null;
    dateTaskApproved = null;
    courtOrderedTask = false;
    courtMandatedClosure = false;
    task = null;
    dateTaskCreated = null;
    dateTaskCompleted = null;
    dateCourtAction = null;
  }

  public int getCaseId() {
    return caseId;
  }

  public boolean isCourtOrderedTask() {
    return courtOrderedTask;
  }
  
  public boolean isCourtMandatedClosure() {
    return courtMandatedClosure;
  }

  public org.exolab.castor.types.Date getDateTaskCompleted() {
    return dateTaskCompleted;
  }

  public org.exolab.castor.types.Date getDateTaskCreated() {
    return dateTaskCreated;
  }
  
  public org.exolab.castor.types.Date getDateCourtAction() {
    return dateCourtAction;
  }

  public int getFamilyPlanEventId() {
    return familyPlanEventId;
  }

  public int getFamilyPlanItemId() {
    return familyPlanItemId;
  }

  public Date getFamilyPlanTaskDateLastUpdate() {
    return familyPlanTaskDateLastUpdate;
  }

  public Date getDateTaskApproved() {
    return dateTaskApproved;
  }

  public int getFamilyPlanTaskId() {
    return familyPlanTaskId;
  }

  public int getStageId() {
    return stageId;
  }

  public String getTask() {
    return task;
  }

  public String getProgress() {
    return progress;
  }
  
  public void setCaseId(int caseId) {
    this.caseId = caseId;
  }

  public void setCourtOrderedTask(boolean courtOrderedTask) {
    this.courtOrderedTask = courtOrderedTask;
  }
  
  public void setCourtMandatedClosure(boolean courtMandatedClosure) {
    this.courtMandatedClosure = courtMandatedClosure;
  }

  public void setDateTaskApproved(Date dateTaskApproved) {
    this.dateTaskApproved = dateTaskApproved;
  }

  public void setDateTaskCompleted(org.exolab.castor.types.Date dateTaskCompleted) {
    this.dateTaskCompleted = dateTaskCompleted;
  }

  public void setDateTaskCreated(org.exolab.castor.types.Date dateTaskCreated) {
    this.dateTaskCreated = dateTaskCreated;
  }

  public void setDateCourtAction(org.exolab.castor.types.Date dateCourtAction) {
    this.dateCourtAction = dateCourtAction;
  }
  
  public void setFamilyPlanEventId(int familyPlanEventId) {
    this.familyPlanEventId = familyPlanEventId;
  }

  public void setFamilyPlanItemId(int familyPlanItemId) {
    this.familyPlanItemId = familyPlanItemId;
  }

  public void setFamilyPlanTaskDateLastUpdate(Date familyPlanTaskDateLastUpdate) {
    this.familyPlanTaskDateLastUpdate = familyPlanTaskDateLastUpdate;
  }

  public void setFamilyPlanTaskId(int familyPlanTaskId) {
    this.familyPlanTaskId = familyPlanTaskId;
  }

  public void setStageId(int stageId) {
    this.stageId = stageId;
  }

  public void setTask(String task) {
    this.task = task;
  }
  
  public void setProgress(String progress) {
    this.progress = progress;
  }
  
}

