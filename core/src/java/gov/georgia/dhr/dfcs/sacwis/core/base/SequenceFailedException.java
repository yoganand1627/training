package gov.georgia.dhr.dfcs.sacwis.core.base;

import gov.georgia.dhr.dfcs.sacwis.core.exception.ArchitectureException;

/**
 * Exception is thrown whenever you try to fetch a sequence for a table, but the fetch operation fails.
 *
 * @author Chris Cowell, 27 March 2002
 */
public class SequenceFailedException extends ArchitectureException {
  public SequenceFailedException(String message, Exception e, int priority) {
    super(message, e, priority);
  }

  public SequenceFailedException(String message, Exception e) {
    super(message, e, ArchitectureException.WARNING_PRIORITY);
  }
}







