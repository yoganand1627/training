package gov.georgia.dhr.dfcs.sacwis.structs.casewatch;

import java.io.Serializable;

@SuppressWarnings("serial")
public class CaseWatchSI implements Serializable {

  private int idPerson;
  private int idCase;
  private int idStage;
  private int idUser;
  private String szStageCd;
  
  public int getIdPerson() {
    return idPerson;
  }

  public void setIdPerson(int idPerson) {
    this.idPerson = idPerson;
  }

  public int getIdCase() {
    return idCase;
  }

  public void setIdCase(int idCase) {
    this.idCase = idCase;
  }

  public int getIdStage() {
    return idStage;
  }

  public void setIdStage(int idStage) {
    this.idStage = idStage;
  }

  public int getIdUser() {
    return idUser;
  }

  public void setIdUser(int idUser) {
    this.idUser = idUser;
  }
  
  public String getSzStageCd() {
    return szStageCd;
  }

  public void setSzStageCd(String szStageCd) {
    this.szStageCd = szStageCd;
  }

}