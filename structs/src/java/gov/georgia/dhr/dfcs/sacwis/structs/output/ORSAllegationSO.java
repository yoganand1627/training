package gov.georgia.dhr.dfcs.sacwis.structs.output;

import java.io.Serializable;
import java.util.Date;

public class ORSAllegationSO implements Serializable {
  
  String szPrimaryType = null;
  
  String szSecondaryType = null;
  
  String szFinding = null;
  
  String szRestatement = null;
  
  String szNarrative = null;
  
  Date dtLastUpdate = null;

  public String getSzPrimaryType() {
    return szPrimaryType;
  }

  public void setSzPrimaryType(String szPrimaryType) {
    this.szPrimaryType = szPrimaryType;
  }

  public String getSzSecondaryType() {
    return szSecondaryType;
  }

  public void setSzSecondaryType(String szSecondaryType) {
    this.szSecondaryType = szSecondaryType;
  }

  public String getSzFinding() {
    return szFinding;
  }

  public void setSzFinding(String szFinding) {
    this.szFinding = szFinding;
  }

  public String getSzRestatement() {
    return szRestatement;
  }

  public void setSzRestatement(String szRestatement) {
    this.szRestatement = szRestatement;
  }

  public String getSzNarrative() {
    return szNarrative;
  }

  public void setSzNarrative(String szNarrative) {
    this.szNarrative = szNarrative;
  }

  public Date getDtLastUpdate() {
    return dtLastUpdate;
  }

  public void setDtLastUpdate(Date dtLastUpdate) {
    this.dtLastUpdate = dtLastUpdate;
  }

}
