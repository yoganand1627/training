package gov.georgia.dhr.dfcs.sacwis.structs.output;

public class AddressValidatorSO {

  private String addressNumber;
  private String street1;
  private String street2;
  private String thoroughFare;
  private String postalDirection;
  private String city;
  private String state;
  private String zipCode;
  private String zip4;
  private String countyCode;
  private String formattedStreetAddress;
  private boolean validated;
  private String dpvStatus;
  private double x;
  private double y;

  public AddressValidatorSO() {
  }

  public AddressValidatorSO(String addressNumber, String street1, String street2, String thoroughFare,
                            String postalDirection, String city, String state, String zipCode, String zip4,
                            String countyCode, String formattedStreetAddress, boolean validated, double x, double y) {
    
    this(addressNumber, street1, street2, thoroughFare, postalDirection, city, state, zipCode, zip4,
                       countyCode, formattedStreetAddress, validated, x, y, null);
    
  }
  
  public AddressValidatorSO(String addressNumber, String street1, String street2, String thoroughFare,
                            String postalDirection, String city, String state, String zipCode, String zip4,
                            String countyCode, String formattedStreetAddress, boolean validated, double x, double y, String dpvStatus) {
    this.addressNumber = addressNumber;
    this.street1 = street1;
    this.street2 = street2;
    this.thoroughFare = thoroughFare;
    this.postalDirection = postalDirection;
    this.city = city;
    this.state = state;
    this.zipCode = zipCode;
    this.zip4 = zip4;
    this.countyCode = countyCode;
    this.formattedStreetAddress = formattedStreetAddress;
    this.validated = validated;
    this.x = x;
    this.y = y;
    this.dpvStatus = dpvStatus;
  }

  public String getAddressNumber() {
    return addressNumber;
  }

  public void setAddressNumber(String addressNumber) {
    this.addressNumber = addressNumber;
  }

  public String getStreet1() {
    return street1;
  }

  public void setStreet1(String street1) {
    this.street1 = street1;
  }

  public String getStreet2() {
    return street2;
  }

  public void setStreet2(String street2) {
    this.street2 = street2;
  }

  public String getPostalDirection() {
    return postalDirection;
  }

  public void setPostalDirection(String postalDirection) {
    this.postalDirection = postalDirection;
  }

  public String getThoroughFare() {
    return thoroughFare;
  }

  public void setThoroughFare(String thoroughFare) {
    this.thoroughFare = thoroughFare;
  }

  public String getCity() {
    return city;
  }

  public void setCity(String city) {
    this.city = city;
  }

  public String getState() {
    return state;
  }

  public void setState(String state) {
    this.state = state;
  }

  public String getZip4() {
    return zip4;
  }

  public void setZip4(String zip4) {
    this.zip4 = zip4;
  }

  public String getZipCode() {
    return zipCode;
  }

  public void setZipCode(String zipCode) {
    this.zipCode = zipCode;
  }

  public String getFormattedStreetAddress() {
    return formattedStreetAddress;
  }

  public void setFormattedStreetAddress(String formattedStreetAddress) {
    this.formattedStreetAddress = formattedStreetAddress;
  }

  public boolean isValidated() {
    return validated;
  }

  public void setValidated(boolean validated) {
    this.validated = validated;
  }

  public double getX() {
    return x;
  }

  public void setX(double x) {
    this.x = x;
  }

  public double getY() {
    return y;
  }

  public void setY(double y) {
    this.y = y;
  }

  public String getCountyCode() {
    return countyCode;
  }

  public void setCountyCode(String countyCode) {
    this.countyCode = countyCode;
  }

  public String getDpvStatus() {
    return dpvStatus;
  }

  public void setDpvStatus(String dpvStatus) {
    this.dpvStatus = dpvStatus;
  }
}