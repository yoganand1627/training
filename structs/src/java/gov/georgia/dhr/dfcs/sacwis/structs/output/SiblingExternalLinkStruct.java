package gov.georgia.dhr.dfcs.sacwis.structs.output;

import java.io.Serializable;

/**
 * The SiblingExternalLinkStruct struct is used to transport all sibling information in a different case
 * to a service in order to save the records to the database.
 * 
 * @author hai.nguyen
 *
 * <PRE>
 * 
 * Date         Updated by                Description
 * ----------   ------------              -------------------------------------
 * 09/06/2011   Hai Nguyen                STGAP00017011: MR-092 Initial check-in.
 * 
 * </PRE>
 */
@SuppressWarnings("serial")
public class SiblingExternalLinkStruct implements Serializable {
	
	int idSiblingGroup;
	
	int idPerson;
	
	int idSiblingExternalStage;

	public int getIdSiblingGroup() {
		return idSiblingGroup;
	}

	public void setIdSiblingGroup(int idSiblingGroup) {
		this.idSiblingGroup = idSiblingGroup;
	}

	public int getIdPerson() {
		return idPerson;
	}

	public void setIdPerson(int idPerson) {
		this.idPerson = idPerson;
	}

	public int getIdSiblingExternalStage() {
		return idSiblingExternalStage;
	}

	public void setIdSiblingExternalStage(int idSiblingExternalStage) {
		this.idSiblingExternalStage = idSiblingExternalStage;
	}

}
