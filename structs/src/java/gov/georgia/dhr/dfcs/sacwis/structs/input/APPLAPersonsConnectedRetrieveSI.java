package gov.georgia.dhr.dfcs.sacwis.structs.input;

import java.io.Serializable;

/**
 * POJO that contains the variables to retrieve persons connected Information
 *
 * @author Bhavna Gehlot, December 02, 2009
 */
@SuppressWarnings("serial")
public class APPLAPersonsConnectedRetrieveSI implements Serializable {
  protected int idEvent = 0;
  protected int idStage = 0;
  protected int idCase = 0;
  protected int idResource = 0;

  public int getIdEvent() {
    return idEvent;
  }

  public void setIdEvent(int idEvent) {
    this.idEvent = idEvent;
  }

  public int getIdStage() {
    return idStage;
  }

  public void setIdStage(int idStage) {
    this.idStage = idStage;
  }
  
  public int getIdCase() {
    return idCase;
  }

  public void setIdCase(int idCase) {
    this.idCase = idCase;
  }
  
  public int getIdResource() {
    return idResource;
  }

  public void setIdResource(int idResource) {
    this.idResource = idResource;
  }
}
