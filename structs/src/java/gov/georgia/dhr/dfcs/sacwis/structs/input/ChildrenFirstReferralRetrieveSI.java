
package gov.georgia.dhr.dfcs.sacwis.structs.input;

import java.io.Serializable;

/**
 * @author ashwini.rege
 *  POJO that contains the variables to retrieve Children First Referral Information
 *
 */

public class ChildrenFirstReferralRetrieveSI implements Serializable {
  int ulIdChild;
  int ulIdStage;
  int ulIdCase;
  int ulIdEvent;
  String cdStage;
  ArchInputStruct archInput;
  
  public int getUlIdChild() {
    return ulIdChild;
  }
  public void setUlIdChild(int ulIdChild) {
    this.ulIdChild = ulIdChild;
  }
  public int getUlIdStage() {
    return ulIdStage;
  }
  public void setUlIdStage(int ulIdStage) {
    this.ulIdStage = ulIdStage;
  }
  public int getUlIdCase() {
    return ulIdCase;
  }
  public void setUlIdCase(int ulIdCase) {
    this.ulIdCase = ulIdCase;
  }
  public int getUlIdEvent() {
    return ulIdEvent;
  }
  public void setUlIdEvent(int ulIdEvent) {
    this.ulIdEvent = ulIdEvent;
  }
  public String getCdStage() {
    return cdStage;
  }
  public void setCdStage(String cdStage) {
    this.cdStage = cdStage;
  }
  public ArchInputStruct getArchInput() {
    return archInput;
  }
  public void setArchInput(ArchInputStruct archInput) {
    this.archInput = archInput;
  }

}
