package gov.georgia.dhr.dfcs.sacwis.structs.input;

import java.io.Serializable;

public class ValidateLoginSI  implements Serializable{
  private String userName;
  private String password;
  private Integer numOfFailedLogins;
  private String crudFlag; //Flag that represents which field needs to be updated
  private Integer numOfPrevPasswords;
  private Integer idUser;
  private String ipAddr;
  private String indLoginSuccess;
  
  public String getUserName() {
    return userName;
  }
  public void setUserName(String userName) {
    this.userName = userName;
  }
  public String getPassword() {
    return password;
  }
  public void setPassword(String password) {
    this.password = password;
  }
  public Integer getNumOfFailedLogins() {
    return numOfFailedLogins;
  }
  public void setNumOfFailedLogins(Integer numOfFailedLogins) {
    this.numOfFailedLogins = numOfFailedLogins;
  }
  public String getCrudFlag() {
    return crudFlag;
  }
  public void setCrudFlag(String crudFlag) {
    this.crudFlag = crudFlag;
  }
  public Integer getNumOfPrevPasswords() {
    return numOfPrevPasswords;
  }
  public void setNumOfPrevPasswords(Integer numOfPrevPasswords) {
    this.numOfPrevPasswords = numOfPrevPasswords;
  }
  public Integer getIdUser() {
    return idUser;
  }
  public void setIdUser(Integer idUser) {
    this.idUser = idUser;
  }
  public String getIpAddr() {
    return ipAddr;
  }
  public void setIpAddr(String ipAddr) {
    this.ipAddr = ipAddr;
  }
  public String getIndLoginSuccess() {
    return indLoginSuccess;
  }
  public void setIndLoginSuccess(String indLoginSuccess) {
    this.indLoginSuccess = indLoginSuccess;
  }
  
}
