package gov.georgia.dhr.dfcs.sacwis.structs.input;

import java.io.Serializable;

public class AdoptionResourceBean implements Serializable {

  private int idResource;

  private int idAdoption;

  private String nmResource;

  public AdoptionResourceBean() {
    super();
  }

  public int getIdAdoption() {
    return idAdoption;
  }

  public void setIdAdoption(int idAdoption) {
    this.idAdoption = idAdoption;
  }

  public int getIdResource() {
    return idResource;
  }

  public void setIdResource(int idResource) {
    this.idResource = idResource;
  }

  public String getNmResource() {
    return nmResource;
  }

  public void setNmResource(String nmResource) {
    this.nmResource = nmResource;
  }

}
