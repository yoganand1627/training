package gov.georgia.dhr.dfcs.sacwis.service.common;

import gov.georgia.dhr.dfcs.sacwis.structs.input.ValidateContactDateSI;



public interface ValidateContactDate {

  /**
   * This service wraps passes in the current resource ID for the placement (portal),
   * user ID, and date of the contact which is being saved to verify that the 
   * date is not prior to the start date of the user with the agency.
   * 
   * This is used only to validate portal contacts.
   *
   * @param validateContactDateSI
   * @return result
   */

  public boolean validateContactDate(ValidateContactDateSI validateContactDateSI);
}
