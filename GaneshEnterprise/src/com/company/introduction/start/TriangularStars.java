package com.company.introduction.start;

public class TriangularStars {

	public static void main(String[] args) {

		int r = 5;//i represents number of rows
		 for (int i=5; i<=r;i++)
		 {
			 for (int st=1;st<=i;st++)
			 {
				 System.out.println("*");
		
			 }
			 	System.out.println("&");
		 }
		}
	}
